# ── Full example programs ─────────────────────────────────────────────────

class TestFullPrograms:
    def test_hello_world(self):
        src = 'func main() -> void { print("Hello, World!") }'
        cpp = generate(src)
        assert "Hello, World!" in cpp

    def test_fibonacci(self):
        src = """
func fibonacci(n: int) -> int {
    if n <= 1 {
        return n
    }
    return fibonacci(n - 1) + fibonacci(n - 2)
}
func main() -> void {
    print(fibonacci(10))
}
"""
        cpp = generate(src)
        assert "fibonacci" in cpp
        assert "return 0;" in cpp

    def test_factorial(self):
        src = """
func factorial(n: int) -> int {
    if n <= 1 { return 1 }
    return n * factorial(n - 1)
}
func main() -> void {
    for i in range(1, 6) { print(factorial(i)) }
}
"""
        cpp = generate(src)
        assert "factorial" in cpp
        assert "for (int i = 1; i < 6; ++i)" in cpp
