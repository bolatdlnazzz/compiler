"""
Unit tests for the ZLang C++ Code Generator.

Rather than compiling and executing the generated C++, these tests verify:
  * that the generated code contains expected C++ constructs (string matching)
  * that the full pipeline (lex → parse → analyze → codegen) succeeds
  * structural properties (includes, function signatures, etc.)
"""
import pytest
from src.lexer import Lexer
from src.parser import Parser
from src.semantic import SemanticAnalyzer
from src.codegen import CodeGenerator


def generate(source: str) -> str:
    """Run the full pipeline and return the generated C++ string."""
    tokens = Lexer(source).tokenize()
    ast = Parser(tokens).parse()
    SemanticAnalyzer().analyze(ast)
    return CodeGenerator().generate(ast)


# ── Header / preamble ─────────────────────────────────────────────────────

class TestHeader:
    def test_includes_iostream(self):
        cpp = generate("func main() -> void {}")
        assert "#include <iostream>" in cpp

    def test_includes_string(self):
        cpp = generate("func main() -> void {}")
        assert "#include <string>" in cpp

    def test_has_generator_comment(self):
        cpp = generate("func main() -> void {}")
        assert "Generated by ZLang compiler" in cpp


