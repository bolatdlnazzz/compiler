# ── Control flow ──────────────────────────────────────────────────────────

class TestControlFlow:
    def test_if_statement(self):
        cpp = generate("func f() -> void { if true { print(1) } }")
        assert "if (true)" in cpp

    def test_if_else_statement(self):
        cpp = generate("func f() -> void { if true { print(1) } else { print(2) } }")
        assert "} else {" in cpp

    def test_while_loop(self):
        cpp = generate("func f() -> void { let x: int = 0  while x < 5 { x = x + 1 } }")
        assert "while (" in cpp
        assert "x < 5" in cpp

    def test_for_loop(self):
        cpp = generate("func f() -> void { for i in range(0, 10) { print(i) } }")
        assert "for (int i = 0; i < 10; ++i)" in cpp


